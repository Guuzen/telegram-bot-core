declare(strict_types = 1);

namespace ServiceBus\TelegramBot\Api\Type\Message;

/**
 * Message identifier.
 */
final class MessageId
{
    /**
     * @var string
     */
    private $value;

    /**
     * @param string $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function toString(): string
    {
        return $this->value;
    }
}
